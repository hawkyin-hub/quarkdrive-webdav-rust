#!/usr/bin/env bash
# Stage 13b.1: install the LocalQuark privileged helper as a
# LaunchDaemon. The helper runs as root and accepts XPC connections
# from run-localquark / teardown-localquark to perform mount_webdav
# and unmount operations that the user-context launchd agent cannot.
#
# Lifecycle:
#   - First install: sudo prompt, then bootstrap into system domain.
#   - Idempotent: re-running with the same version is a no-op.
#   - Upgrade: if helper version differs from on-disk version, bootout
#     + replace binary + bootstrap again.
#
# Versioning: helper/.build/release/LocalQuarkHelper binary prints
# its version via `client version`. We persist the installed version
# to ~/Library/Application Support/LocalQuark/.helper-version.
#
# Why sudo + cp + launchctl bootstrap instead of SMJobBless:
#   SMJobBless requires a .app bundle with SMAuthorizedClients in
#   Info.plist, plus a signed app to bootstrap from. We are a single
#   Mach-O binary, no bundle. `sudo + cp + launchctl bootstrap system`
#   does the same thing with one shell pipeline.
set -euo pipefail
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
LOCALQUARK_REPO_ROOT="$(cd "$SCRIPT_DIR/.." && pwd)"
# shellcheck source=lib-common.sh
source "$SCRIPT_DIR/lib-common.sh"

LABEL="com.localquark.webdav-helper"
DEST_HELPER="/Library/PrivilegedHelperTools/$LABEL"
DEST_PLIST="/Library/LaunchDaemons/$LABEL.plist"
VERSION_FILE="$LOCALQUARK_APP_DIR/.helper-version"
HELPER_BIN="$LOCALQUARK_REPO_ROOT/helper/.build/release/LocalQuarkHelper"
SOURCE_PLIST="$LOCALQUARK_REPO_ROOT/helper/Resources/$LABEL.plist"

ensure_app_dir

if [ ! -x "$HELPER_BIN" ]; then
    log "helper: binary not built; running helper/build.sh"
    "$LOCALQUARK_REPO_ROOT/helper/build.sh"
fi

# 1. read built version
NEW_VERSION="$("$HELPER_BIN" client version)"
OLD_VERSION=""
[ -f "$VERSION_FILE" ] && OLD_VERSION="$(cat "$VERSION_FILE" 2>/dev/null || true)"

# 2. check whether helper is already loaded (system domain)
helper_loaded() {
    launchctl print "system/$LABEL" >/dev/null 2>&1
}

if helper_loaded && [ "$OLD_VERSION" = "$NEW_VERSION" ]; then
    log "helper: version unchanged ($NEW_VERSION); no-op"
    exit 0
fi

# 3. we need to install or upgrade: sudo prompt + cp + bootstrap.
#    sudo here is required because /Library/{PrivilegedHelperTools,
#    LaunchDaemons} are root-owned. launchctl bootstrap in the
#    system domain also requires root.
log "helper: install/upgrade requested ($OLD_VERSION -> $NEW_VERSION)"

# If a previous version is loaded, bootout first so cp does not
# fail with "text file busy". Use sudo for bootout because the
# system-domain launchctl print returned earlier but bootout can
# still race with helper holding the Mach port.
if helper_loaded; then
    log "helper: bootout system/$LABEL (replacing running helper)"
    sudo launchctl bootout "system/$LABEL" 2>/dev/null || \
        log "WARN: bootout rc=$? (continuing)"
fi

log "helper: sudo install $HELPER_BIN -> $DEST_HELPER"
sudo install -m 0755 -o root -g wheel "$HELPER_BIN" "$DEST_HELPER"

log "helper: sudo install $SOURCE_PLIST -> $DEST_PLIST"
# Substitute __HOME__ with the user\'s $HOME so the helper LaunchDaemon
# sees HOME=/Users/HawkSept (not /var/root) and trustCert can resolve
# the cert path. We render to a tmp file in $LOCALQUARK_APP_DIR
# (writable by us) then sudo cp into /Library/LaunchDaemons/.
RENDERED_PLIST="$LOCALQUARK_APP_DIR/.helper.plist"
sed -e "s|__HOME__|$HOME|g" "$SOURCE_PLIST" > "$RENDERED_PLIST"
sudo install -m 0644 -o root -g wheel "$RENDERED_PLIST" "$DEST_PLIST"
rm -f "$RENDERED_PLIST"

log "helper: sudo launchctl bootstrap system $DEST_PLIST"
sudo launchctl bootstrap system "$DEST_PLIST"

# 4. persist version
echo "$NEW_VERSION" > "$VERSION_FILE"
chmod 0644 "$VERSION_FILE"

# 5. verify the service is actually accepting connections. We wait
#    briefly for launchd to finish bootstrapping, then probe via the
#    client-mode `version` invocation. Failure here would mean the
#    Mach service name in the plist does not match main.swift.
sleep 2
if "$DEST_HELPER" client version >/dev/null 2>&1; then
    log "helper: installed and reachable (version=$NEW_VERSION)"
else
    rc=$?
    log "helper: WARN: installed but client probe failed rc=$rc"
    log "helper: see /var/log/com.localquark.webdav-helper.err.log"
    exit "$rc"
fi

# 6. Stage 14.2: bootstrap the System keychain trust for the
#    self-signed cert generated by setup-tls.sh. We do this once
#    here (when the user is already in a sudo prompt for the helper
#    install) so the LaunchAgent context does not need to sudo
#    later. We use the same sudo context the helper install just
#    established -- no extra prompt.
#
#    The cert is regenerated on every cold start, so a one-time
#    trust is not strictly enough; the run-localquark.sh will call
#    `helper trust-cert` after every cold start to re-trust. The
#    initial trust here is for the case where the user runs
#    install-helper.sh before any cold start -- the cert file
#    $LOCALQUARK_CERT_DIR/cert.pem may not exist yet, in which case
#    we skip and let run-localquark.sh handle it.
if [ -f "$LOCALQUARK_CERT_DIR/cert.pem" ]; then
    log "install: trusting cert in System keychain (sudo add-trusted-cert)"
    if sudo /usr/bin/security add-trusted-cert -d -r trustRoot \
            -k /Library/Keychains/System.keychain \
            "$LOCALQUARK_CERT_DIR/cert.pem" 2>>"$LOCALQUARK_LOG_FILE"; then
        log "install: cert trusted in System keychain"
    else
        rc=$?
        log "install: WARN: add-trusted-cert failed (rc=$rc); helper will retry on next cold start"
    fi
else
    log "install: no cert at $LOCALQUARK_CERT_DIR yet; helper will trust on first cold start"
fi
